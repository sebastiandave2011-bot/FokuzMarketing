document.addEventListener("DOMContentLoaded", () => {
    
    // --- 1. Lógica Original del Selector de Idiomas ---
    const btnEs = document.getElementById("btn-es");
    const btnEn = document.getElementById("btn-en");
    let currentLang = localStorage.getItem("language") || "es";
    setLanguage(currentLang);

    if (btnEs && btnEn) {
        btnEs.addEventListener("click", () => setLanguage("es"));
        btnEn.addEventListener("click", () => setLanguage("en"));
    }

    function setLanguage(lang) {
        localStorage.setItem("language", lang);
        if (lang === "es" && btnEs && btnEn) {
            btnEs.classList.add("active");
            btnEn.classList.remove("active");
        } else if (btnEn && btnEs) {
            btnEn.classList.add("active");
            btnEs.classList.remove("active");
        }
        document.querySelectorAll("[data-key]").forEach(elem => {
            const key = elem.getAttribute("data-key");
            if (translations[lang] && translations[lang][key]) {
                elem.textContent = translations[lang][key];
            }
        });
    }

    // --- 2. Lógica Original del Intersection Observer ---
    const fadeElements = document.querySelectorAll('.fade-in');
    const observerOptions = { root: null, rootMargin: '0px', threshold: 0.15 };
    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target); 
            }
        });
    }, observerOptions);
    fadeElements.forEach(elem => observer.observe(elem));

    // --- 3. Lógica Original del Showcase Carrusel (MOTOR JS) ---
    const showcaseContainer = document.querySelector('.showcase-container');
    const dragWrapper = document.querySelector('.showcase-drag-wrapper');
    const galleryItems = document.querySelectorAll('.gallery-item-cover');
    
    const modal = document.getElementById('image-modal');
    const modalImg = document.getElementById('modal-image');
    const modalCloseBtn = document.querySelector('.modal-close');

    let isDraggingWrapper = false, startPos = 0, currentTranslate = 0, prevTranslate = 0, didDrag = false, hasUserInteracted = false, isHovered = false;
    let animSpeed = 0.15, animDirection = -1; 

    if (showcaseContainer && dragWrapper) {
        const animateGallery = () => {
            if (!isDraggingWrapper && !hasUserInteracted && !isHovered) {
                const minTranslate = showcaseContainer.clientWidth - dragWrapper.scrollWidth;
                if (minTranslate < 0) {
                    currentTranslate += (animSpeed * animDirection);
                    if (currentTranslate <= minTranslate) { currentTranslate = minTranslate; animDirection = 1; } 
                    else if (currentTranslate >= 0) { currentTranslate = 0; animDirection = -1; }
                    dragWrapper.style.transform = `translateX(${currentTranslate}px)`;
                    prevTranslate = currentTranslate;
                }
            }
            requestAnimationFrame(animateGallery);
        };
        requestAnimationFrame(animateGallery);

        showcaseContainer.addEventListener('mouseenter', () => { isHovered = true; });
        showcaseContainer.addEventListener('mouseleave', () => { isHovered = false; });
        document.addEventListener('click', (e) => { if (!showcaseContainer.contains(e.target) && hasUserInteracted) hasUserInteracted = false; });

        const getPositionX = (e) => e.type.includes('mouse') ? e.pageX : e.touches[0].clientX;

        const startDrag = (e) => {
            isDraggingWrapper = true; didDrag = false; hasUserInteracted = true; startPos = getPositionX(e);
            dragWrapper.classList.add('dragging');
        };
        const drag = (e) => {
            if (!isDraggingWrapper) return;
            const currentPosition = getPositionX(e);
            const diff = currentPosition - startPos;
            if (Math.abs(diff) > 5) didDrag = true; 
            currentTranslate = prevTranslate + diff;
            const minTranslate = showcaseContainer.clientWidth - dragWrapper.scrollWidth;
            if (currentTranslate > 0) currentTranslate = 0; 
            if (currentTranslate < minTranslate) currentTranslate = minTranslate; 
            dragWrapper.style.transform = `translateX(${currentTranslate}px)`;
        };
        const endDrag = () => {
            if(!isDraggingWrapper) return;
            isDraggingWrapper = false; dragWrapper.classList.remove('dragging'); prevTranslate = currentTranslate; 
        };

        dragWrapper.addEventListener('mousedown', startDrag);
        window.addEventListener('mousemove', drag);
        window.addEventListener('mouseup', endDrag);
        window.addEventListener('mouseleave', endDrag); 
        dragWrapper.addEventListener('touchstart', startDrag, {passive: true});
        window.addEventListener('touchmove', drag, {passive: true});
        window.addEventListener('touchend', endDrag);

        showcaseContainer.addEventListener('wheel', (e) => {
            if (Math.abs(e.deltaX) > 0) {
                e.preventDefault(); hasUserInteracted = true; currentTranslate -= (e.deltaX * 1.5); 
                const minTranslate = showcaseContainer.clientWidth - dragWrapper.scrollWidth;
                if (currentTranslate > 0) currentTranslate = 0;
                if (currentTranslate < minTranslate) currentTranslate = minTranslate;
                prevTranslate = currentTranslate;
                dragWrapper.style.transform = `translateX(${currentTranslate}px)`;
            }
        }, { passive: false });

        const openModal = (imgSrc) => { if (!modal || !modalImg) return; modalImg.src = imgSrc; modal.classList.add('active'); document.body.classList.add('modal-open'); };
        const closeModal = () => { if (!modal) return; modal.classList.remove('active'); document.body.classList.remove('modal-open'); setTimeout(() => { modalImg.src = ""; }, 300); };

        galleryItems.forEach(item => {
            item.addEventListener('click', (e) => { e.preventDefault(); if (didDrag) return; const img = item.querySelector('img'); if (img) openModal(img.src); });
            const img = item.querySelector('img'); if (img) img.addEventListener('dragstart', (e) => e.preventDefault());
        });

        if (modalCloseBtn) modalCloseBtn.addEventListener('click', closeModal);
        if (modal) { modal.addEventListener('click', (e) => { if (e.target === modal) closeModal(); }); }
        document.addEventListener('keydown', (e) => { if (e.key === 'Escape' && modal.classList.contains('active')) closeModal(); });
    }

    // --- 4. Lógica Original del Modal del Desarrollador ---
    const devModal = document.getElementById('dev-modal');
    const footerLogo = document.querySelector('.footer-logo');
    const devModalCloseBtn = document.querySelector('.dev-modal-close');

    if (footerLogo && devModal) {
        const openDevModal = () => { devModal.classList.add('active'); document.body.classList.add('modal-open'); };
        const closeDevModal = () => { devModal.classList.remove('active'); if (!document.getElementById('image-modal').classList.contains('active')) document.body.classList.remove('modal-open'); };
        footerLogo.addEventListener('click', openDevModal);
        if (devModalCloseBtn) devModalCloseBtn.addEventListener('click', closeDevModal);
        devModal.addEventListener('click', (e) => { if (e.target === devModal) closeDevModal(); });
        document.addEventListener('keydown', (e) => { if (e.key === 'Escape' && devModal.classList.contains('active')) closeDevModal(); });
    }

    // --- 5. NUEVO: Control del Menú Hamburguesa ---
    const hamburger = document.getElementById('hamburger-menu');
    const sideMenu = document.getElementById('side-menu');
    const closeMenu = document.querySelector('.close-menu');
    if(hamburger && sideMenu) {
        hamburger.addEventListener('click', () => sideMenu.classList.add('active'));
        closeMenu.addEventListener('click', () => sideMenu.classList.remove('active'));
        document.querySelectorAll('#side-menu a').forEach(link => {
            link.addEventListener('click', () => sideMenu.classList.remove('active'));
        });
    }

    // --- 6. NUEVO: Buscador Global con Resaltado ---
    const btnSearch = document.getElementById('btn-search');
    const searchInput = document.getElementById('global-search');

    if(btnSearch && searchInput) {
        const runSearch = () => {
            const query = searchInput.value.trim().toLowerCase();
            if(!query) return;

            document.querySelectorAll('.highlight-text').forEach(el => {
                const parent = el.parentNode;
                parent.replaceChild(document.createTextNode(el.textContent), el);
                parent.normalize();
            });

            const elements = document.querySelectorAll('h1, h2, h3, p');
            let found = false;

            elements.forEach(node => {
                if(node.closest('.focuz-header') || node.closest('script')) return;
                const text = node.textContent.toLowerCase();
                if(text.includes(query) && node.children.length === 0) {
                    const regex = new RegExp(`(${query})`, 'gi');
                    node.innerHTML = node.innerHTML.replace(regex, '<span class="highlight-text">$1</span>');
                    if(!found) {
                        node.scrollIntoView({ behavior: 'smooth', block: 'center' });
                        found = true;
                    }
                }
            });
            searchInput.value = '';
        };

        btnSearch.addEventListener('click', runSearch);
        searchInput.addEventListener('keypress', (e) => {
            if(e.key === 'Enter') runSearch();
        });
    }

    // --- 7. INTERACTIVIDAD AVANZADA LOGO (Cámara & Susurro) ---
    const interactiveTarget = document.querySelector('.interactive-letter');
    
    // Instanciamos los audios multimedia (Colócalos en tu directorio Assets/)
    const cameraSound = new Audio('Assets/click.MP3'); 
    const whisperSound = new Audio('Assets/Fokuz.MP3'); 

    if (interactiveTarget) {
        interactiveTarget.addEventListener('click', () => {
            // Evitamos la superposición reiniciando los buffers de audio
            cameraSound.currentTime = 0;
            whisperSound.currentTime = 0;

            // 1. Ejecutar sonido e impacto visual de la cámara (Flash)
            cameraSound.play().catch(err => console.log("Interacción de audio bloqueada por el navegador"));
            interactiveTarget.classList.add('camera-flash');

            // Limpiamos el flash de la cámara al terminar su ciclo de animación
            setTimeout(() => {
                interactiveTarget.classList.remove('camera-flash');
            }, 350);

            // 2. Retardo calculado para que el susurro y el barrido entren justo tras el click
            setTimeout(() => {
                whisperSound.play().catch(err => console.log(err));
                interactiveTarget.classList.add('whisper-glow');
            }, 400);

            // Quitamos el efecto de barrido brillante para poder repetir el bucle en futuros clicks
            setTimeout(() => {
                interactiveTarget.classList.remove('whisper-glow');
            }, 1900);
        });
    }

    // --- 8. Envío Directo a Correo con EmailJS (Sin salir de la página) ---
    emailjs.init("4EZ9ZuktjzCYul8Rr");

    const contactForm = document.getElementById('contact-form');

    if (contactForm) {
        contactForm.addEventListener('submit', function(event) {
            event.preventDefault(); 

            const submitBtn = contactForm.querySelector('button[type="submit"]');
            const originalText = submitBtn.textContent;
            submitBtn.textContent = "Enviando...";
            submitBtn.disabled = true;

            emailjs.sendForm('service_6p71b0g', 'template_baw1ozm', this)
                .then(() => {
                    alert('¡Gracias! Tu solicitud ha sido enviada con éxito directamente a nuestro equipo.');
                    contactForm.reset(); 
                })
                .catch((error) => {
                    alert('Ocurrió un error al enviar el mensaje. Por favor, intenta de nuevo.');
                    console.log('Error de EmailJS:', error);
                })
                .finally(() => {
                    submitBtn.textContent = originalText;
                    submitBtn.disabled = false;
                });
        });
    }
});